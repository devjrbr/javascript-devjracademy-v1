// O que é uma Api ?
`
  Application Programming Interfaces
  Uma API é como um mensageiro que permite que diferentes aplicativos de software se comuniquem entre si.
  Use uma analogia do mundo real, como fazer um pedido de comida em um restaurante através de um garçom. 
  O garçom (API) anota o seu pedido (requisição), se comunica com a cozinha (servidor) e traz a comida (resposta).
`
// Definições de API
`
um conjunto de regras e protocolos que governam como os componentes de software devem interagir.
APIs permitem que os desenvolvedores utilizem funcionalidades pré-construídas de outros sistemas 
de software sem precisar entender seu funcionamento interno.
`
