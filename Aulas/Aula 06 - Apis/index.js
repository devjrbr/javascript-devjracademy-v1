// Componentes da API
`
Requisição: As informações enviadas à API para realizar uma ação específica ou recuperar dados.
Resposta: Os dados retornados pela API em resposta à requisição.
`
// Workflow da API
`
O cliente (aplicativo do desenvolvedor) envia uma requisição para o servidor (API) com parâmetros específicos.
O servidor processa a requisição e realiza as ações necessárias.
O servidor envia de volta uma resposta contendo os dados solicitados ou informações sobre a ação realizada.
`
